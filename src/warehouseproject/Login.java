    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
      

        if (IDField.getText().length() == 0) // Checking for empty field
        {
            JOptionPane.showMessageDialog(null, "Empty fields detected ! Please fill up ID fields");
        } else if (PassField.getPassword().length == 0) // Checking for empty field
        {
            JOptionPane.showMessageDialog(null, "Empty fields detected ! Please fill up PASSWORD fields");
        } else {
            String user = IDField.getText();   // Collecting the input
            nameLogin = user;
            char[] pass = PassField.getPassword(); // Collecting the input
            String pwd = String.copyValueOf(pass);  // converting from array to string
            if (validate_login(user, pwd)) {
                hide();
                
                    MainMenu m = new MainMenu();
                    m.setVisible(true);
                    JOptionPane.showMessageDialog(null, "Login Successful");
  
            } else {
                JOptionPane.showMessageDialog(null, "Incorrect Login Credentials");
                CancelButtonActionPerformed(evt);
            }
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

