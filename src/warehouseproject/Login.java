    //This method to combine all the keypress 'Enter' button
    private void doButton(java.awt.event.KeyEvent evt) {
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (IDField.getText().length() == 0) // Checking for empty field
            {
                JOptionPane.showMessageDialog(null, "Please fill up ID fields");
            } else if (PassField.getPassword().length == 0) // Checking for empty field
            {
                JOptionPane.showMessageDialog(null, "Please fill up PASSWORD fields");
            } else {
                String user = IDField.getText();   // Collecting the input
                char[] pass = PassField.getPassword(); // Collecting the input
                nameLogin = user;
                String pwd = String.copyValueOf(pass);  // converting from array to string
                if (validate_login(user, pwd)) {
                    hide();

                    MainMenu m = new MainMenu();
                    m.setVisible(true);
                    JOptionPane.showMessageDialog(null, "Login Successful");

                } else {
                    JOptionPane.showMessageDialog(null, "Incorrect Login Credentials", "Access Denied!", JOptionPane.ERROR_MESSAGE);
                    clear();
                }
            }
        }
    }

