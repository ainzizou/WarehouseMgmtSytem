    private boolean validate_login(String user, String pwd) {
        try {
//            Class.forName("com.mysql.jdbc.Driver");  // MySQL database connection
//            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/warehouse?" + "user=root&password=haloain");
            conn = DbConnection.ConnectDB();
            PreparedStatement pst = conn.prepareStatement("select * from staff where staffID=? and password=?");
            pst.setString(1, user);
            pst.setString(2, pwd);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                return true;
            } else {
                return false;
            }
        } catch (SQLException e) {
            return false;
        }
    }
